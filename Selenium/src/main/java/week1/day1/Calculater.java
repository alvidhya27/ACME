package week1.day1;

public class Calculater {
	int c;
	//Addition Method
	public int add(int a,int b)
	{
		c=a+b;
		return c;
	}
	
	//Subtraction Method
	public int subtraction(int a,int b)
	{
		c=a-b;
		return c;
	}
	
	//Multiplication Method
	public int multiplication(int a,int b)
	{
		c=a*b;
		return c;
	}
	
	//division Method
	public int division(int a,int b)
	{
		c=a/b;
		return c;
	}
	
}
	